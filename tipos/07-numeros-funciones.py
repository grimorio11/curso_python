import math

print(round(2.5)) # <class 'int'> (redondeo)
print(abs(-2)) # <class 'int'> (valor absoluto)
print(abs(-2.5)) # <class 'float'> (valor absoluto)
print(pow(2, 3)) # <class 'int'> (potencia)
print(pow(2.5, 3)) # <class 'float'> (potencia)
print(max(1, 2, 3)) # <class 'int'> (maximo)

print(math.ceil(2.5)) # <class 'int'> (redondeo hacia arriba)
print(math.floor(2.5)) # <class 'int'> (redondeo hacia abajo)
print(math.isnan(2.5)) # <class 'bool'> (es NaN)
print(math.pow(2, 3)) # <class 'int'> (potencia)